/*Aqui será mostrados o Operador de Atribuição e algumas
variações do Operador de Atribuição atráves de Operações
Aritméticas.
*/

/* 
    "=" - Atribuição 
    Exemplo:
*/
const a = 7;
let b = 3;


 /*   
    "+=" - Atribuição Adtiva 
    Exemplo:
*/
b += a //b = b + a;
console.log(b);


/*   
    "-=" - Atribuição Subtrativa
    Exemplo:
*/
b -= 4;// b = b - 4;
console.log(b);


/*
    "*=" - Atribuição Multiplicativa
    Exemplo:
*/
b *= 2;// b = b * 2;
console.log(b);

/*
    "/=" - Atribuição Divisiva 
    Exemplo:
*/
b /= 2; // b = b / 2;
console.log(b);


/*
    "%=" - Atribuição Modular - Resto da Divição da Operação, Se
    o resto da divição for Par o resultado vai ser 0, se o valor
    for ímpa o valor vai ser 1.
    Exemplo:
*/
b %= 2; //b = b % 2;
console.log(b);


